/**
 * Footer icon interface
 */
export interface FooterIconsInterface {
  /**
   * Name can be optional
   */
  name?: string;

  /**
   * Icon for footer
   */
  icon: any;

  /**
   * Link for icon
   */
  ref: string;
}

/**
 * Footer label interface
 */
export interface FooterLabelInterface {
  /**
   * Label for footer
   */
  label: string;
}
